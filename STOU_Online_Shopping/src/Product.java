
public class Product {
	
	String id;
	String name;
	Double price;
	
	public String getId() {
		return id;
	}
	public void setId(String xid) {
		this.id = xid;
	}
	public String getName() {
		return name;
	}
	public void setName(String xname) {
		this.name = xname;
	}
	public Double getUsername() {
		return price;
	}
	public void setUsername(Double xprice) {
		this.price = xprice;
	}

}
